package com.office.vo.adjustment;

public class adjustmentInfoVO {
	
	private String adjustmentDt;
	private String orderPrice;
	private String sellingQta;
	
	public String getAdjustmentDt() {
		return adjustmentDt;
	}
	public void setAdjustmentDt(String adjustmentDt) {
		this.adjustmentDt = adjustmentDt;
	}
	public String getOrderPrice() {
		return orderPrice;
	}
	public void setOrderPrice(String orderPrice) {
		this.orderPrice = orderPrice;
	}
	public String getSellingQta() {
		return sellingQta;
	}
	public void setSellingQta(String sellingQta) {
		this.sellingQta = sellingQta;
	}

}
